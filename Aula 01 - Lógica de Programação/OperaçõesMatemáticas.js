function soma(num1, num2) {
    return num1 + num2;
}
function subtracao(num1, num2) {
    return num1 - num2
}
function divisao(num1, num2) {
    return num1 / num2
}
function multiplicacao(num1, num2) {
    return num1 * num2
}
function resto(num1, num2) {
    return num1 % num2
}
console.log('Soma de 5 + 10: ' + soma (5,10));
console.log('Subtração de 10 - 5: ' + subtracao (10,5));
console.log('Divisão de 10 / 5: ' + divisao (10,5));
console.log('Multiplicação de 5 * 10: ' + multiplicacao (5,10));
console.log('Resto de 10 % 5: ' + resto (10,5));